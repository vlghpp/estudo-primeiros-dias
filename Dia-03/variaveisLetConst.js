//No JavaScript, tem-se três tipos de forma para se definir uma váriavel -> var, let e const
//Mas usaremos daqui para frente apenas o let e o const por que é a forma mais atualizada de se usar js

//Exemplos:

const nome = "Henrique Patricio";//Estou definindo uma váriavel que está guardando meu nome Henrique Patricio
//e que não vai mudar, por isso 'const' que é igual a constante, ou seja, não muda

let idade = 19; //Estou definindo uma váriavel que está guardando minha idade, mas note que agora ela é let
//o que significa que ela pode ser alterada, ano que vem não vou ter só mais 19 vou ter 20, por isso o let

console.log(nome);
console.log(idade);