//Bom, agora vou explicar a vocês a parte sobre índice de um array, mas o que é índice de um array?
//O índice vai ser a 'posição' de um elemento dentro de um array, ficou confuso? Deixa eu tentar explicar melhor:

let Arry = []; //Como já vimos antes, essa é a estrutura padrão de um array

Arry[0] =  "João"; //O que está acontecendo aqui? Veja, pegamos nosso array (Arry), e agora colocamos um índice ao seu lado Arry[0]
//esse índice significa a posição em que o elemento está, ou seja, se ele estiver na posição 0, logo vai ser o nosso primeiro valor!

//entenda como funciona um Array: [0, 1, 2, 3, 4, 5, 6, .....]
//Vai ser assim sempre os índices, sempre começando no 0, então nosso primeiro valor não vai ser o 1, mas sim o 0!!!!!!
//E agora para acessar-mos esse nosso "João" que foi definido para ser encontrado no indice [0](primeiro valor)
//É fácil, veja:

console.log(Arry[0])  //Estamos falando pro computador que queremos que ele pegue nosso Array criado por nós (Arry), e nos mostre o que
//está no índice 0 dele. Lembre-se de como é feito a ordem dos elementos dentro de um Array ->
// ---> [0(primeira posição), 1(segunda posição), 2(terceira posição), 3(quarta posição), 4(quinta posição), ......] E assim vai!


//Podemos também já chegar e criar um valor específico para nosso Array, veja:

